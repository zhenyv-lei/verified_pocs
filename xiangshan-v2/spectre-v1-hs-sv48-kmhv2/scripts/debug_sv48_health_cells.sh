#!/usr/bin/env bash
set -euo pipefail

ROOT=${ROOT:-/nfs/home/leizhenyu/opt/testbench}
RUN_DIR=${RUN_DIR:-$ROOT/targets/xiangshan/logs/spectre-v1-sv48-fullstring-recovery-20260717}
EMU=${EMU:-/nfs/home/leizhenyu/opt/DUTs/XiangShan/build/emu}
AM_HOME=${AM_HOME:-$ROOT/.local/nexus-am-kmhv2}
ARCH=${ARCH:-riscv64-xs}
MARCH=${MARCH:-rv64gch_zba_zbb_zbc_zbs_zbkb_zbkc_zbkx_zknd_zkne_zknh_zkr_zksed_zksh_zkt_zicbom_zicboz}
MODE=${MODE:-hs}
case "$MODE" in
  hs|vu|vs) ;;
  *)
    echo "unknown MODE=$MODE" >&2
    exit 2
    ;;
esac
PREFIX=${PREFIX:-$MODE}
WORKLOAD=${WORKLOAD:-$ROOT/targets/xiangshan/workloads/spectre-v1-${MODE}-sv48-kmhv2}
NAME=${NAME:-spectre-v1-${MODE}-sv48-kmhv2}
TIMEOUT_SEC=${TIMEOUT_SEC:-180}
CELL=${CELL:-entry}
MATCH_GRACE_SEC=${MATCH_GRACE_SEC:-2}

ART_DIR="$RUN_DIR/health-cells"
mkdir -p "$ART_DIR"

case "$CELL" in
  entry)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=80 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=1 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=1 -DTRAIN_TIMES=1 -DEARLY_STOP=0 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=1 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DSMOKE_ENTRY_ONLY=1"
    EXPECT_PATTERN="[${PREFIX}] done s=17"
    PASS_PATTERN="$EXPECT_PATTERN"
    ;;
  control)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=80 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=1 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=1 -DTRAIN_TIMES=1 -DEARLY_STOP=0 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=1 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DCONTROL_ONLY=1"
    EXPECT_PATTERN="[${PREFIX}] done s=0"
    PASS_PATTERN="$EXPECT_PATTERN"
    ;;
  onebyte)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=50 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=1 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=3 -DTRAIN_TIMES=6 -DEARLY_STOP=0 -DEARLY_STOP_MIN_SCORE=3 -DEARLY_STOP_GAP=2 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=256 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DROUNDS=1 -DCONTROL_ONLY=0 -DFILTER_KNOWN_NOISE=0 -DUSE_BASELINE_SUBTRACT=0"
    EXPECT_PATTERN=" r="
    PASS_PATTERN="[${PREFIX}] b0 e=53 g=53"
    ;;
  onebyte-fl1)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=50 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=1 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=3 -DTRAIN_TIMES=6 -DEARLY_STOP=0 -DEARLY_STOP_MIN_SCORE=3 -DEARLY_STOP_GAP=2 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=1 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DROUNDS=1 -DCONTROL_ONLY=0 -DFILTER_KNOWN_NOISE=0 -DUSE_BASELINE_SUBTRACT=0"
    EXPECT_PATTERN=" r="
    PASS_PATTERN="$EXPECT_PATTERN"
    ;;
  onebyte-fl62)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=50 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=1 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=3 -DTRAIN_TIMES=6 -DEARLY_STOP=0 -DEARLY_STOP_MIN_SCORE=3 -DEARLY_STOP_GAP=2 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=62 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DROUNDS=1 -DCONTROL_ONLY=0 -DFILTER_KNOWN_NOISE=0 -DUSE_BASELINE_SUBTRACT=0"
    EXPECT_PATTERN=" r="
    PASS_PATTERN="$EXPECT_PATTERN"
    ;;
  stage-after-attack-flush)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=50 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=1 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=3 -DTRAIN_TIMES=6 -DEARLY_STOP=0 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=1 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DROUNDS=1 -DSMOKE_AFTER_ATTACK_FLUSH_ONLY=1"
    EXPECT_PATTERN="[${PREFIX}] done s=33"
    PASS_PATTERN="$EXPECT_PATTERN"
    ;;
  stage-after-first-victim)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=50 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=1 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=3 -DTRAIN_TIMES=6 -DEARLY_STOP=0 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=1 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DROUNDS=1 -DSMOKE_AFTER_FIRST_VICTIM_ONLY=1"
    EXPECT_PATTERN="[${PREFIX}] done s=34"
    PASS_PATTERN="$EXPECT_PATTERN"
    ;;
  stage-after-training)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=50 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=1 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=3 -DTRAIN_TIMES=6 -DEARLY_STOP=0 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=1 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DROUNDS=1 -DSMOKE_AFTER_TRAINING_ONLY=1"
    EXPECT_PATTERN="[${PREFIX}] done s=35"
    PASS_PATTERN="$EXPECT_PATTERN"
    ;;
  stage-after-probe)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=50 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=1 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=3 -DTRAIN_TIMES=6 -DEARLY_STOP=0 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=1 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DROUNDS=1 -DSMOKE_AFTER_PROBE_ONLY=1"
    EXPECT_PATTERN="[${PREFIX}] done s=36"
    PASS_PATTERN="$EXPECT_PATTERN"
    ;;
  fullstring-smoke)
    CPPFLAGS_CELL="-DCACHE_HIT_THRESHOLD=80 -DUSE_FIXED_CACHE_HIT_THRESHOLD=1 -DSECRET_SZ=6 -DSECRET_OFFSET=0 -DATTACK_SAME_ROUNDS=3 -DTRAIN_TIMES=6 -DEARLY_STOP=0 -DEARLY_STOP_MIN_SCORE=3 -DEARLY_STOP_GAP=2 -DPROBE_CANDIDATES=62 -DFULL_BYTE_PROBE=0 -DFLUSH_LINES=256 -DSMOKE_FAST_BOOT=1 -DSMOKE_TRACE=1 -DROUNDS=1 -DCONTROL_ONLY=0 -DFILTER_KNOWN_NOISE=0 -DUSE_BASELINE_SUBTRACT=0"
    EXPECT_PATTERN="[${PREFIX}] b5"
    PASS_PATTERN="$EXPECT_PATTERN"
    EXPECT_REGEX="^\\[${PREFIX}\\] b5 .* r="
    PASS_REGEX="$EXPECT_REGEX"
    ;;
  *)
    echo "unknown CELL=$CELL" >&2
    exit 2
    ;;
esac

ART_TAG=${ART_TAG:-${MODE}-${CELL}}
BUILD_LOG="$ART_DIR/${ART_TAG}-build.log"
RUN_LOG="$ART_DIR/${ART_TAG}-run.log"
CMD_LOG="$ART_DIR/${ART_TAG}.cmd"
ELF_SRC="$WORKLOAD/build/${NAME}-${ARCH}.elf"
ELF_DST="$ART_DIR/${ART_TAG}-${NAME}-${ARCH}.elf"
VERDICT="$ART_DIR/${ART_TAG}.verdict"

{
  echo "CELL=$CELL"
  echo "MODE=$MODE"
  echo "PREFIX=$PREFIX"
  echo "WORKLOAD=$WORKLOAD"
  echo "CPPFLAGS=$CPPFLAGS_CELL"
  echo "TIMEOUT_SEC=$TIMEOUT_SEC"
  echo "EXPECT_PATTERN=$EXPECT_PATTERN"
  echo "PASS_PATTERN=$PASS_PATTERN"
  echo "EXPECT_REGEX=${EXPECT_REGEX:-}"
  echo "PASS_REGEX=${PASS_REGEX:-}"
  echo "EMU=$EMU --no-diff -i $ELF_DST"
} > "$CMD_LOG"

cd "$WORKLOAD"
AM_HOME="$AM_HOME" make clean > "$BUILD_LOG" 2>&1
CPPFLAGS="$CPPFLAGS_CELL" CC_OPT=-O2 make \
  ARCH="$ARCH" \
  MARCH="$MARCH" \
  LINUX_GNU_TOOLCHAIN=1 \
  AM_HOME="$AM_HOME" >> "$BUILD_LOG" 2>&1

cp "$ELF_SRC" "$ELF_DST"
sha256sum "$ELF_DST" > "$ART_DIR/${ART_TAG}.sha256"

{
  date -Is
  echo "$EMU --no-diff -i $ELF_DST"
} > "$RUN_LOG"

"$EMU" --no-diff -i "$ELF_DST" >> "$RUN_LOG" 2>&1 &
emu_pid=$!
deadline=$((SECONDS + TIMEOUT_SEC))
matched=0

while kill -0 "$emu_pid" 2>/dev/null; do
  if [ -n "${EXPECT_REGEX:-}" ] && rg -q "$EXPECT_REGEX" "$RUN_LOG"; then
    matched=1
    break
  fi
  if [ -z "${EXPECT_REGEX:-}" ] && rg -q -F "$EXPECT_PATTERN" "$RUN_LOG"; then
    matched=1
    break
  fi
  if [ "$SECONDS" -ge "$deadline" ]; then
    break
  fi
  sleep 1
done

if kill -0 "$emu_pid" 2>/dev/null; then
  if [ "$matched" -eq 1 ] && [ "$MATCH_GRACE_SEC" -gt 0 ]; then
    sleep "$MATCH_GRACE_SEC"
  fi
  kill "$emu_pid" 2>/dev/null || true
  sleep 1
  kill -9 "$emu_pid" 2>/dev/null || true
fi
wait "$emu_pid" 2>/dev/null || true

{
  echo "EMU_MONITOR_DONE matched=$matched"
  date -Is
} >> "$RUN_LOG"

if [ -n "${PASS_REGEX:-}" ] && rg -q "$PASS_REGEX" "$RUN_LOG"; then
  echo "PASS cell=$CELL matched_regex=$PASS_REGEX" > "$VERDICT"
  exit 0
fi

if [ -z "${PASS_REGEX:-}" ] && rg -q -F "$PASS_PATTERN" "$RUN_LOG"; then
  echo "PASS cell=$CELL matched=$PASS_PATTERN" > "$VERDICT"
  exit 0
fi

if [ -n "${EXPECT_REGEX:-}" ] && rg -q "$EXPECT_REGEX" "$RUN_LOG"; then
  echo "FAIL cell=$CELL observed_regex=$EXPECT_REGEX missing_pass_regex=${PASS_REGEX:-}" > "$VERDICT"
  exit 0
fi

if [ -z "${EXPECT_REGEX:-}" ] && rg -q -F "$EXPECT_PATTERN" "$RUN_LOG"; then
  echo "FAIL cell=$CELL observed=$EXPECT_PATTERN missing_pass=$PASS_PATTERN" > "$VERDICT"
  exit 0
fi

if [ "$SECONDS" -ge "$deadline" ]; then
  echo "TIMEOUT cell=$CELL missing=$EXPECT_PATTERN" > "$VERDICT"
  exit 124
fi

echo "FAIL cell=$CELL missing=$EXPECT_PATTERN" > "$VERDICT"
exit 1
